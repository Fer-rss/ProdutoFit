    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int linhaSelecionada = tblClientes.getSelectedRow();

        if (linhaSelecionada >= 0) {
            // Mensagem para confirmar a exclusão
            int resposta = JOptionPane.showConfirmDialog(
                    this,"Tem certeza que deseja remover o cliente \"" + txtNome.getText() + "\"?",
                    "Confirmação",
                    JOptionPane.YES_NO_OPTION);

            if (resposta == JOptionPane.YES_OPTION) {
                // Remove da lista
                Cliente c = listaClientes.get(linhaSelecionada);
                dao.excluir(c.getId());
                JOptionPane.showMessageDialog(this, "Cliente excluido com sucesso!");
                // Atualiza a tabela
                atualizarTabela();
                // Limpa os campos
                limparCampos();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um cliente para excluir!");
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        // Ler os dados dos campos
        String nome = txtNome.getText().trim();
        String cpf = txtCpf.getText().trim();
        String endereco = txtEndereco.getText().trim();
        String email = txtEmail.getText().trim();
        String telefone = txtTelefone.getText().trim();

        // Validar os principais campos
        if (nome.isEmpty() || cpf.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha Nome e CPF!");
            return;
        }

        // Criar objeto Cliente
        Cliente cliente = new Cliente();
        cliente.setNomeCliente(nome);
        cliente.setCpf(cpf);
        cliente.setEndereco(endereco);
        cliente.setEmail(email);
        cliente.setTelefone(telefone);

        // Adicionar à lista
        dao.cadastrar(cliente);
        JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!");
        // Atualizar a tabela
        atualizarTabela();

        // Limpar os campos
        limparCampos();
    }//GEN-LAST:event_btAdicionarActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        int linhaSelecionada = tblClientes.getSelectedRow();
        if (linhaSelecionada >= 0) {
            Cliente c = listaClientes.get(linhaSelecionada);
            c.setNomeCliente(txtNome.getText().trim());
            c.setCpf(txtCpf.getText().trim());
            c.setEndereco(txtEndereco.getText().trim());
            c.setEmail(txtEmail.getText().trim());
            c.setTelefone(txtTelefone.getText().trim());

            dao.atualizar(c);
            JOptionPane.showMessageDialog(this, "Cliente alterado com sucesso!");
            atualizarTabela();
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um cliente para alterar!");
        }
    }//GEN-LAST:event_btAlterarActionPerformed


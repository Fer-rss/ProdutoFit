    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        // Ler os dados dos campos
        String nome = txtNome.getText().trim();
        String cnpj = txtCnpj.getText().trim();
        String prazo = txtPrazo.getText().trim();
        
        //Validar os principais dados
        if (nome.isEmpty() || cnpj.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha Nome e CNPJ!");
            return;
        }
        
        // Cria objeto Fornecedor
        Fornecedor fornecedor = new Fornecedor();
        fornecedor.setNomeFornecedor(nome);
        fornecedor.setCnpj(cnpj);
        fornecedor.setPrazoEntrega(prazo);
        
        // Adicionar à lista     
        dao.cadastrar(fornecedor);

        JOptionPane.showMessageDialog(this, "Fornecedor cadastrado com sucesso!");

        // Atualizar a tabela
        atualizarTabela();

        // Limpar os campos
        limparCampos();    
    }//GEN-LAST:event_btAdicionarActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        int linhaSelecionada = tblFornecedor.getSelectedRow();
            if (linhaSelecionada >= 0) {
                Long id = (Long) modeloTabela.getValueAt(linhaSelecionada, 0);
                Fornecedor f = new Fornecedor();
                f.setId(id);
                f.setNomeFornecedor(txtNome.getText().trim());
                f.setCnpj(txtCnpj.getText().trim());
                f.setPrazoEntrega(txtPrazo.getText().trim());

                dao.atualizar(f);
               
                JOptionPane.showMessageDialog(this, "Fornecedor alterado com sucesso!");

                atualizarTabela();
                limparCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Selecione um fornecedor para alterar!");
        }
    }//GEN-LAST:event_btAlterarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int linhaSelecionada = tblFornecedor.getSelectedRow();

        if (linhaSelecionada >= 0) {
            // Mensagem para confirmar a exclusão
            int resposta = JOptionPane.showConfirmDialog(
                    this,"Tem certeza que deseja remover o fornecedor \"" + txtNome.getText() + "\"?",
                    "Confirmação",
                    JOptionPane.YES_NO_OPTION);

            if (resposta == JOptionPane.YES_OPTION) {
                // Remove da lista
                Long id = (Long) modeloTabela.getValueAt(linhaSelecionada, 0);
                dao.excluir(id);
                JOptionPane.showMessageDialog(this, "Fornecedor excluido com sucesso!");
                // Atualiza a tabela
                atualizarTabela();
                // Limpa os campos
                limparCampos();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um fornecedor para excluir!");
        }
    }//GEN-LAST:event_btExcluirActionPerformed


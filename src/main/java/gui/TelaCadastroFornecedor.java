            private void configurarTabela() {
                modeloTabela = (DefaultTableModel) tblFornecedor.getModel();
                modeloTabela.setRowCount(0);
            }

            private void atualizarTabela() {
                modeloTabela.setRowCount(0);
                listaFornecedor = new ArrayList<>(dao.listarTodos());

                for (Fornecedor f : listaFornecedor) {
                    modeloTabela.addRow(new Object[]{
                        f.getId(),
                        f.getNomeFornecedor(),
                        f.getCnpj(),
                        f.getPrazoEntrega()
                    });
                }
            }

            private void limparCampos() {
                txtNome.setText("");
                txtCnpj.setText("");
                txtPrazo.setText("");
            }
